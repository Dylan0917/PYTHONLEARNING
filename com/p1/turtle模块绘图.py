import turtle

turtle.pensize(4)
turtle.pencolor("red")
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.goto(40, 40)
turtle.up()
turtle.goto(-40, 40)
turtle.down()
turtle.forward(100)
turtle.begin_fill()
turtle.circle(50)
turtle.fillcolor("blue")
turtle.end_fill()


turtle.done()